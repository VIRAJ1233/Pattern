
public class NumAscDesPattern {
	
		public static void main(String[] args) {
			int a = 7;
			int j;
			int i;
			for( i = 0 ;i<=a;i++) {
				System.out.println();
				for( j = 1; j<=i;j++) {
					System.out.print(j);
					
				}
				}
			
			for ( i = a-1; i >=1; i--)  
			{  
				System.out.println();
			for (j = 1; j <= i; j++)  
			{  
			System.out.print(j);  
			}  
			  
			}  
					
				
					}
			}

